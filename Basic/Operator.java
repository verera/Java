package Basic;

/**
 * Operator
 */
public class Operator {

    public static void main(String[] args) {
        
        /**
         * Operator Aritmatika
         * 
         * Addition         [ + ]
         * Subtraction      [ - ]
         * Multiplication   [ * ]
         * Division         [ / ]
         * Modulus          [ % ]
         * Increment        [ ++ ]
         * Decrement        [ -- ]
         */
        

        /**
         * Operator Penugasan
         * 
         *  =   [x = 5] [x = 5]
         *  +=   [x += 5] [x += 5] 
         *  -=   [x -= 5] [x -= 5] 
         *  *=   [x *= 5] [x *= 5] 
         *  /=   [x /= 5] [x /= 5] 
         *  %=   [x %= 5] [x %= 5]
         */

        /**
         * Operator Perbandingan
         * 
         * ==   [x == y]
         * ===   [x === y]
         * !=   [x != y]
         *  >   [x > y]
         *  <   [x < y]
         * >=   [x >= y]
         * <=   [x <= y]
         */

        /**
         * Operator Logika
         * 
         * &&   [x > 5 && x < 10]
         * ||   [x > 5 || x < 10]
         * !   !(x < 5 && x < 10)
         */
        
    }
}
